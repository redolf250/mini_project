#define FORCE_CURSOR_TO_SECOND_ROW 0xC0
#define FORCE_CURSOR_TO_THIRD_ROW 0x94
#define FORCE_CURSOR_TO_FORTH_ROW 0xD4

#define CLEAR_SCREEN 0x01
#define RETURN_HOME 0x02
#define DECREMENT_CURSOR 0x04
#define INCREMENT_CURSOR 0x06
#define SHIFT_DISPLAY_LEFT 0x08
#define DISPLAY_OFF_CURSOR_OFF 0x08
#define DISPLAY_OFF_CURSOR_ON 0x0A
#define DISPLAY_ON_CURSOR_OFF 0x0C
#define DISPLAY_ON_CURSOR_BLINKING 0x0E
#define DISPLAY_OFF_CURSOR_BLINKING 0x0F
#define SHIFT_CURSOR_LEFT 0x10
#define SHIFT_CURSOR_RIGHT 0x14
#define SHIFT_ENTIRE_DISPLAY_LEFT 0x18
#define SHIFT_ENTIRE_DISPLAY_RIGHT 0x1C




